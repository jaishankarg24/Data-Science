import matplotlib.pyplot as mp 
x=[10,20,30,40,50]
y=[15,25,35,45,55]

mp.plot(x,y)
mp.title('line graph')
mp.xlabel('x axis')
mp.ylabel('y axis')

mp.show()

# NOTE:
# -----
# -->Inorder to obtain line graph we have to make use of plot() available within pyplot submodule , 
# matplotlib module
# -->Inorder to display any graph we have to use show() 